                Scaffold(modifier = Modifier.fillMaxSize()) {
                    Counter()
                }
            }
        }
    }
}

@Composable
fun Counter() {
    var counter by rememberSaveable { mutableStateOf(0) }

    Column(
        modifier = Modifier
            .fillMaxSize(),
        horizontalAlignment = Alignment.CenterHorizontally,
        verticalArrangement = Arrangement.Center
    ) {
        Button(
            onClick = {}
        ) { Text("Increase")}
            Text(
                counter.toString(),
                fontSize = 64.sp
        )
        Button(
            onClick = {}
        ) { Text("Decrease")}}
        }


